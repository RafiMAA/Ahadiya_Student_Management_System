async def _school_annual(db, styles, title_style, subtitle_style, settings, academic_year_id, **_):
    year_label = await db.fetchval("SELECT year_label FROM academic_years WHERE id = $1", academic_year_id)
    elements = _header_elements(settings, title_style, subtitle_style,
                                 f"School Annual Attendance Report", f"Academic Year {year_label}")
    classes = await db.fetch(
        """SELECT c.id, c.grade, c.medium, c.gender_type FROM classes c
           WHERE c.academic_year_id = $1 ORDER BY c.medium, c.grade""", academic_year_id)

    data = []
    for cls in classes:
        name = f"Grade {cls['grade']} {cls['medium']} {cls['gender_type']}"
